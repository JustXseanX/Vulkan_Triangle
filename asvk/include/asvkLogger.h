    #define DLOGW( fmt, ... )      asvk::SystemLogger::GetInstance().LogW( asvk::LogLevel::Debug, ASVK_WIDE("[File: %s, Line: %d] ") ASVK_WIDE(fmt) ASVK_WIDE("\n"), ASVK_WIDE(__FILE__), __LINE__, ##__VA_ARGS__ )
  #else
    #define DLOGW( fmt, ... )      ((void)0)
  #endif//defined(DEBUG) || defined(_DEBUG)
