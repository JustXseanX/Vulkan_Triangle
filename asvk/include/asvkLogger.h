#endif//ELOGA

#ifndef ELOGW
#define ELOGW( fmt, ... )      asvk::SystemLogger::GetInstance().LogW( asvk::LogLevel::Error, ASVK_WIDE("[File: %s, Line: %d] ") ASVK_WIDE(fmt) ASVK_WIDE("\n"), ASVK_WIDE(__FILE__), __LINE__, ##__VA_ARGS__ )
#endif//ELOGW

